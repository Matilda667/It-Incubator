// Создайте приложение, которое будет отображать количество километров на литр(выход) на основе количества миль на британский галлон(входные данные).

// Ваш ответ должен быть точным в пределах 0,01 kpl.

// Некоторые полезные ассоциации, относящиеся к этому ката:

// 1 имперский галлон = 4, 54609188 литра
// 1 миля = 1, 609344 км

function converter(mpg) {
    const kilometersPerMile = 1.609344;
    const litersPerImperialGallon = 4.54609188;

    // Конвертация
    const kpl = (mpg * kilometersPerMile) / litersPerImperialGallon;

    // Округление до 2 знаков после запятой
    return Math.round(kpl * 100) / 100;
}

// Пример использования
console.log(converter(10)); // Вывод: 3.52
console.log(converter(20)); // Вывод: 7.04
console.log(converter(30)); // Вывод: 10.56